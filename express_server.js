const express = require("express");
const bcrypt = require("bcrypt");
const bodyParser = require("body-parser"); // convert input to readable string in req.body
const cookieSession = require("cookie-session");
const {
  generateRandomString,
  getUserByEmail,
  urlsForUser,
} = require("./helpers");

const app = express();
app.use(bodyParser.urlencoded({ extended: true }));
app.use(
  cookieSession({
    name: "session",
    keys: ["key1", "key2"],
  })
);
app.set("view engine", "ejs");

const PORT = 8080;
const urlDatabase = {}; // b6UTxQ: { longURL: "http://www.tsn.ca", userID: "aJ48lW" },
const users = {}; // userRandomID: { id: "userRandomID",email: "user@example.com", password: "hashed-password", },
const LENGTH = 6; // length of random string to be generated by generateRandomString()

/* ROUTES
1. GET /register
2. POST /register
3. GET /login
4. POST /login
5. POST /logout
6. GET /urls - list of user's URLs
7. GET /urls/new - form for creating URL - route must come before /urls/:shortURL
8. POST /urls - create new URL
9. GET /urls/:shortURL - details about a specific URL; form for updating URL
10. POST /urls/:shortURL - update URL
11. POST /urls/:shortURL/delete
12. GET /u/:shortURL - redirect to longURL
*/
app.get("/register", (req, res) => {
  const templateVars = {
    user: users[req.session.userID],
  };
  res.render("register", templateVars);
});

app.post("/register", (req, res) => {
  const inputEmail = req.body.email;
  const inputPassword = req.body.password;
  const existingUser = getUserByEmail(inputEmail, users);
  if (inputEmail && inputPassword && !existingUser) {
    const userID = generateRandomString(LENGTH);
    const hashedPassword = bcrypt.hashSync(inputPassword, 10);
    users[userID] = {
      id: userID,
      email: inputEmail,
      password: hashedPassword,
    };
    req.session.userID = userID;
    return res.redirect("/urls");
  }
  res
    .status(400)
    .send(
      '<h1 class="h3 mb-3">400 Bad Request</h1><p>You entered the email or password incorrectly or used an existing email address. Please return to the <a href="/register">register</a> page.</p>'
    );
});

app.get("/login", (req, res) => {
  const templateVars = {
    user: users[req.session.userID],
  };
  res.render("login", templateVars);
});

app.post("/login", (req, res) => {
  const inputEmail = req.body.email;
  const existingUser = getUserByEmail(inputEmail, users);
  const inputPassword = req.body.password;
  if (
    existingUser &&
    bcrypt.compareSync(inputPassword, existingUser.password)
  ) {
    req.session.userID = existingUser.id;
    return res.redirect("/urls");
  }
  res
    .status(403)
    .send(
      '<h1 class="h3 mb-3">403 Forbidden</h1><p>The username or password you entered is incorrect. Please <a href="/login">login</a> or <a href="/register">register</a>.</p>'
    );
});

app.post("/logout", (req, res) => {
  req.session = null;
  res.redirect("/login");
});

app.get("/urls", (req, res) => {
  const userIDCookie = req.session.userID;
  if (userIDCookie) {
    const templateVars = {
      urls: urlsForUser(userIDCookie, urlDatabase),
      user: users[userIDCookie],
    };
    return res.render("urls_index", templateVars);
  }
  res
    .status(401)
    .send(
      '<h1 class="h3 mb-3">401 Unauthorized</h1><p>You need to <a href="/login">login</a> or <a href="/register">register</a> before you can see this page.</p>'
    );
});

app.get("/urls/new", (req, res) => {
  const userIDCookie = req.session.userID;
  if (userIDCookie) {
    const templateVars = {
      user: users[userIDCookie],
    };
    return res.render("urls_new", templateVars);
  }
  res.redirect("/login");
});

app.post("/urls", (req, res) => {
  const shortURL = generateRandomString(LENGTH);
  const longURL = req.body.longURL;
  const userIDCookie = req.session.userID;
  urlDatabase[shortURL] = { longURL: longURL, userID: userIDCookie };
  res.redirect(`/urls/${shortURL}`);
});

app.get("/urls/:shortURL", (req, res) => {
  const inputURL = req.params.shortURL;
  const userIDCookie = req.session.userID;
  if (!(inputURL in urlDatabase)) {
    return res
      .status(404)
      .send(
        '<h1 class="h3 mb-3">404 Not Found</h1><p>Sorry, this page doesn\'t exist.</p>'
      );
  }
  if (userIDCookie !== urlDatabase[inputURL].userID) {
    return res
      .status(401)
      .send(
        '<h1 class="h3 mb-3">401 Unauthorized</h1><p>You are not authorized to see this page.</p>'
      );
  }
  const templateVars = {
    shortURL: inputURL,
    longURL: urlDatabase[inputURL].longURL,
    user: users[userIDCookie],
  };
  res.render("urls_show", templateVars);
});

app.post("/urls/:shortURL", (req, res) => {
  const inputURL = req.params.shortURL;
  const userIDCookie = req.session.userID;
  const newLongURL = req.body.newLongURL;
  if (!(inputURL in urlDatabase)) {
    return res
      .status(400)
      .send(
        '<h1 class="h3 mb-3">400 Bad Request</h1><p>Sorry, this record doesn\'t exist.</p>'
      );
  }
  if (!newLongURL) {
    return res
      .status(400)
      .send(
        "<h1 class=\"h3 mb-3\">400 Bad Request</h1><p>The URL wasn't updated because you didn't submit a new URL.</p>"
      );
  }
  if (userIDCookie !== urlDatabase[inputURL].userID) {
    return res
      .status(401)
      .send(
        '<h1 class="h3 mb-3">401 Unauthorized</h1><p>You are not authorized to update this record.</p>'
      );
  }
  urlDatabase[inputURL] = {
    longURL: newLongURL,
    userID: userIDCookie,
  };
  res.redirect("/urls");
});

app.post("/urls/:shortURL/delete", (req, res) => {
  const inputURL = req.params.shortURL;
  const userIDCookie = req.session.userID;
  if (!(inputURL in urlDatabase)) {
    return res
      .status(400)
      .send(
        '<h1 class="h3 mb-3">400 Bad Request</h1><p>Sorry, this record doesn\'t exist.</p>'
      );
  }
  if (userIDCookie !== urlDatabase[inputURL].userID) {
    return res
      .status(401)
      .send(
        '<h1 class="h3 mb-3">401 Unauthorized</h1><p>You are not authorized to delete this record.</p>'
      );
  }
  delete urlDatabase[inputURL];
  res.redirect("/urls");
});

app.get("/u/:shortURL", (req, res) => {
  const longURL = urlDatabase[req.params.shortURL].longURL;
  res.redirect(longURL);
});

app.listen(PORT, () => {
  console.log(`Example app listening on port ${PORT}!`);
});
